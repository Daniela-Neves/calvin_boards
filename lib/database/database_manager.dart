class DatabaseManager {
  Future<Database> getDatabase() async {
    final path = join(await getDatabasesPath(), 'calvinboards.db');
    return openDatabase(path, version: 1, onCreate: _onCreate);
  }

  _onCreate(Database db, _) async {
    await db.execute(_cadastros);
    await db.execute(_plantacoes);
    await db.execute(_insertPlantacoes);
    await db.execute(_createEquipamentos);
    await db.execute(_insertEquipamentos);
    await db.execute(_createAgroClima);
    await db.execute(_insertAgroClimaCanaSp);
    await db.execute(_insertAgroClimaCanaGo);
  }

